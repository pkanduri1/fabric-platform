-- Benchmark results table
CREATE TABLE CM3INT.BATCH_BENCHMARK_RESULTS (
    RUN_ID              VARCHAR2(64) PRIMARY KEY,
    TIMESTAMP           TIMESTAMP NOT NULL,
    TOTAL_RECORDS       NUMBER(12) NOT NULL,
    THROUGHPUT_PER_SEC  NUMBER(10,2) NOT NULL,
    DURATION_MS         NUMBER(12) NOT NULL,
    PEAK_MEMORY_BYTES   NUMBER(14) NOT NULL,
    AVG_CHUNK_TIME_MS   NUMBER(10,2) NOT NULL,
    ERROR_COUNT         NUMBER(8) NOT NULL,
    RESULT              VARCHAR2(20) NOT NULL,
    CREATED_AT          TIMESTAMP DEFAULT CURRENT_TIMESTAMP
);

-- Guardrail violations table
CREATE TABLE CM3INT.BATCH_GUARDRAIL_VIOLATIONS (
    ID                  NUMBER GENERATED ALWAYS AS IDENTITY PRIMARY KEY,
    RUN_ID              VARCHAR2(64),
    GUARDRAIL_NAME      VARCHAR2(64) NOT NULL,
    THRESHOLD           NUMBER(10,2) NOT NULL,
    ACTUAL_VALUE        NUMBER(10,2) NOT NULL,
    MODE                VARCHAR2(10) NOT NULL,
    STEP_NAME           VARCHAR2(128),
    CHUNK_INDEX         NUMBER(8),
    VIOLATION_TIME      TIMESTAMP NOT NULL,
    MESSAGE             VARCHAR2(512),
    CREATED_AT          TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    CONSTRAINT FK_VIOLATION_RUN FOREIGN KEY (RUN_ID) REFERENCES CM3INT.BATCH_BENCHMARK_RESULTS(RUN_ID)
);

CREATE INDEX IDX_VIOLATIONS_RUN_ID ON CM3INT.BATCH_GUARDRAIL_VIOLATIONS(RUN_ID);
CREATE INDEX IDX_BENCHMARK_TIMESTAMP ON CM3INT.BATCH_BENCHMARK_RESULTS(TIMESTAMP);
